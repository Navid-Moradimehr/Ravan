from __future__ import annotations

import logging

logger = logging.getLogger(__name__)


class SimulatorMetrics:
    def __init__(self, protocol: str, port: int) -> None:
        self.protocol = protocol
        try:
            from prometheus_client import Counter, start_http_server
            self._counter = Counter("industrial_simulator_events_generated_total", "Values generated by protocol simulators", ["protocol"])
            self._start_http_server = start_http_server
        except Exception:  # pragma: no cover
            self._counter = None
            self._start_http_server = None
        self.port = port

    def start(self) -> None:
        if self._start_http_server is None:
            return
        try:
            self._start_http_server(self.port)
        except OSError as exc:
            logger.warning("simulator metrics server unavailable on port %s: %s", self.port, exc)

    def increment(self, amount: int = 1) -> None:
        if self._counter is not None:
            self._counter.labels(protocol=self.protocol).inc(amount)
